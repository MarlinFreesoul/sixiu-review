font.size = Pt(11)
style.element.rPr.rFonts.set(qn('w:eastAsia'), '宋体')

def add_heading(text, level=1):
    h = doc.add_heading(text, level=level)
    for run in h.runs:
        run.font.color.rgb = RGBColor(0x1a, 0x1a, 0x1a)

def add_para(text, bold=False, size=11):
    p = doc.add_paragraph()
    run = p.add_run(text)
    run.bold = bold
    run.font.size = Pt(size)
    run.font.name = '宋体'
    run._element.rPr.rFonts.set(qn('w:eastAsia'), '宋体')

def add_q(q_num, stem, options, answer, analysis, kp):
    """选择题"""
    p = doc.add_paragraph()
    run = p.add_run(f'{q_num}. {stem}')
    run.bold = True; run.font.size = Pt(11)
    for o in options:
        doc.add_paragraph(f'    {o}')
    pa = doc.add_paragraph()
    ra = pa.add_run(f'✅ 答案：{answer}')
    ra.bold = True; ra.font.color.rgb = RGBColor(0x00, 0x66, 0x00)
    doc.add_paragraph(f'📝 分析：{analysis}')
    doc.add_paragraph(f'📖 对应知识点：{kp}')
    doc.add_paragraph()

def add_tf(n, stem, answer, analysis, kp):
    """判断题"""
    p = doc.add_paragraph()
    p.add_run(f'{n}. {stem}').bold = True
    lab = '✅ 正确' if answer == '对' else '❌ 错误'
    pa = doc.add_paragraph()
    ra = pa.add_run(lab); ra.bold = True
    ra.font.color.rgb = RGBColor(0x00,0x66,0x00) if answer == '对' else RGBColor(0xCC,0x00,0x00)
    doc.add_paragraph(f'📝 {"说明" if answer=="对" else "分析"}：{analysis}')
    doc.add_paragraph(f'📖 对应知识点：{kp}')
    doc.add_paragraph()

def add_essay(n, stem, ref):
    """论述题"""
    p = doc.add_paragraph()
    p.add_run(f'{n}. {stem}').bold = True
    doc.add_paragraph(f'📝 参考答案：{ref}')
    doc.add_paragraph()

