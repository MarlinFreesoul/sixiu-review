def add_heading(text, level=1):
    h = doc.add_heading(text, level=level)
    for run in h.runs:
        run.font.color.rgb = RGBColor(0x1a, 0x1a, 0x1a)
    return h

def add_para(text, bold=False, size=11):
    p = doc.add_paragraph()
    run = p.add_run(text)
    run.bold = bold
    run.font.size = Pt(size)
    run.font.name = '宋体'
    run._element.rPr.rFonts.set(qn('w:eastAsia'), '宋体')
    return p

def add_question(q_num, stem, options, answer, analysis, knowledge_point):
    """添加一道选择题（单选/多选）"""
    p = doc.add_paragraph()
    run = p.add_run(f'{q_num}. {stem}')
    run.bold = True
    run.font.size = Pt(11)
    for opt in options:
        doc.add_paragraph(f'    {opt}', style='Normal')
    # 答案
    pa = doc.add_paragraph()
    ra = pa.add_run(f'✅ 答案：{answer}')
    ra.bold = True
    ra.font.color.rgb = RGBColor(0x00, 0x66, 0x00)
    # 分析
    doc.add_paragraph(f'📝 分析：{analysis}', style='Normal')
    # 知识点
    doc.add_paragraph(f'📖 对应知识点：{knowledge_point}', style='Normal')
    doc.add_paragraph()  # spacing

def add_tf(tf_num, stem, answer, analysis, knowledge_point):
    """添加一道判断题"""
    p = doc.add_paragraph()
    run = p.add_run(f'{tf_num}. {stem}')
    run.bold = True
    ans_text = '✅ 正确' if answer == '对' else '❌ 错误'
    pa = doc.add_paragraph()
    ra = pa.add_run(f'{ans_text}')
    ra.bold = True
    ra.font.color.rgb = RGBColor(0x00, 0x66, 0x00) if answer == '对' else RGBColor(0xCC, 0x00, 0x00)
    if answer == '错':
        doc.add_paragraph(f'📝 分析：{analysis}', style='Normal')
    else:
        doc.add_paragraph(f'📝 说明：{analysis}', style='Normal')
    doc.add_paragraph(f'📖 对应知识点：{knowledge_point}', style='Normal')
    doc.add_paragraph()

def add_essay(q_num, stem, ref_answer):
    """添加论述/材料分析题"""
    p = doc.add_paragraph()
    run = p.add_run(f'{q_num}. {stem}')
    run.bold = True
    run.font.size = Pt(12)
    doc.add_paragraph(f'📝 参考答案：{ref_answer}', style='Normal')
    doc.add_paragraph()

